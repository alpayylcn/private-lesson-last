<?php
namespace App\Services\Backend;
use App\Models\Backend\StepOption;
use Illuminate\Http\Request;

class StepOptionService{

    public function __construct(protected StepOption $stepOption){}
       
    public function create(array $stepOptionData){

        return $this->stepOption->create($stepOptionData);
        
    }

    public function update(array $stepOptionData,int $id=0){
        if($id!=0){
            $first=$this->first(['id'=>$id]);
            return $first->update($stepOptionData);
        }
            return false;
        
    }

    public function delete(int $id){

            $first=$this->first(['id'=>$id]);
            return $first->delete();
    }

    public function getWithWhere(array $where = [])
   {
       // Veritabanı sorgusu oluştur
       $query = $this->stepOption->query();

       // 'where' koşullarını eklemek için dizi içinde gelen koşulları döngüye al
       if(is_array($where))
       {
           foreach ($where as $column => $value)
           {
               $query->where($column, $value);
           }
       }
       return $query->orderByDesc('id')->get();
    }

    public function first(array $where)
